// Simple test stream types
pub struct TestAudioStream;

impl Stream for TestAudioStream {
    type Item = i16;

    fn poll_next(self: Pin<&mut Self>, _cx: &mut Context<'_>) -> Poll<Option<Self::Item>> {
        Poll::Ready(None) // Simple empty stream for testing
    }
}

impl Unpin for TestAudioStream {}

// Test segment type that implements TranscriptSegment
#[derive(Clone, Debug, Default)]
pub struct TestSegment {
    pub text: String,
    pub start_ms: u32,
    pub end_ms: u32,
    pub speaker_id: Option<String>,
}

impl fluent_voice::transcript::TranscriptSegment for TestSegment {
    fn start_ms(&self) -> u32 {
        self.start_ms
    }

    fn end_ms(&self) -> u32 {
        self.end_ms
    }

    fn text(&self) -> &str {
        &self.text
    }

    fn speaker_id(&self) -> Option<&str> {
        self.speaker_id.as_deref()
    }
}

pub struct TestTranscriptStream;

impl Stream for TestTranscriptStream {
    type Item = Result<TestSegment, fluent_voice::voice_error::VoiceError>;

    fn poll_next(self: Pin<&mut Self>, _cx: &mut Context<'_>) -> Poll<Option<Self::Item>> {
        Poll::Ready(None) // Simple empty stream for testing
    }
}

impl Unpin for TestTranscriptStream {}

// Simple transcript type for testing
#[derive(Clone, Debug, Default)]
pub struct TestTranscript {
    pub segments: Vec<TestSegment>,
}

