#[cfg(test)]
mod tests {
    use super::*;

    #[tokio::test]
    async fn test_tts_macro_compilation() {
        // Test that the TTS macro generates compilable code
        let _engine = TestTtsEngine;

        // Test builder creation (using trait)
        let _builder = TestTtsEngine::builder();

        // Test engine instance (using trait)
        let engine = TestTtsEngine;
        let _conv_builder = engine.conversation();
    }

    #[tokio::test]
    async fn test_stt_macro_compilation() {
        // Test that the STT macro generates compilable code
        let _engine = TestSttEngine;

        // Test builder creation (using trait)
        let _builder = TestSttEngine::builder();

        // Test engine instance (using trait)
        let engine = TestSttEngine;
        let _conv_builder = engine.conversation();
    }

    #[tokio::test]
    async fn test_tts_speaker_creation() {
        // Test speaker creation using the generated engine
        let speaker = TestTtsEngine::speaker("Alice")
            .speak("Hello, world!")
            .build();

        assert_eq!(speaker.id(), "Alice");
    }

    #[test]
    fn test_macro_types_are_send_sync() {
        // Test that generated types have required bounds
        fn assert_send<T: Send>() {}
        fn assert_sync<T: Sync>() {}
        fn assert_send_sync<T: Send + Sync>() {}

        assert_send_sync::<TestTtsEngine>();
        assert_send_sync::<TestSttEngine>();
    }
}
